package com.ninja.training;

public class PetDataType {
	
	

	public static void main(String[] args) {
		
		//String: Stores Text
		String myPet = "Dog";
		String petName = "Spotty";
		
		//char: Stores Single Character
		char petInitial = 'S';
		
		//int: Stores Integers (Whole Numbers) 
		int petAge = 3; 
		//My dog is 3 years old.
		
		//double: Stores Big Numbers and Decimals
		double petWeight = 10.25;
		//10.25 kilograms
		
		//boolean: true or false
		boolean isPetVaccinated = true;
		boolean willPetBite = false;
		
	}

}
